insert into storage.buckets (id, name, public)
values
  ('avatars', 'avatars', true),
  ('property-assets', 'property-assets', true),
  ('support-attachments', 'support-attachments', false)
on conflict (id) do update
set public = excluded.public;

-- Public read access for uploaded assets.
drop policy if exists "Public read avatars" on storage.objects;
create policy "Public read avatars"
on storage.objects
for select
using (bucket_id = 'avatars');

drop policy if exists "Public read property assets" on storage.objects;
create policy "Public read property assets"
on storage.objects
for select
using (bucket_id = 'property-assets');

drop policy if exists "Authenticated read support attachments" on storage.objects;
create policy "Authenticated read support attachments"
on storage.objects
for select
to authenticated
using (
  bucket_id = 'support-attachments'
  and (storage.foldername(name))[1] = (select auth.uid()::text)
);

-- Users write only inside a folder named after their Supabase user id.
drop policy if exists "Anon upload avatars" on storage.objects;
drop policy if exists "Authenticated upload avatars" on storage.objects;
create policy "Authenticated upload avatars"
on storage.objects
for insert
to authenticated
with check (
  bucket_id = 'avatars'
  and (storage.foldername(name))[1] = (select auth.uid()::text)
);

drop policy if exists "Anon update avatars" on storage.objects;
