-- Supabase bootstrap for Vizare
--
-- This schema supports the current Flutter app structure:
-- - user profiles / roles
-- - property listings
-- - property images
-- - favorites
-- - inquiries
-- - support tickets
-- - storage buckets for avatars and property assets
--
-- Vercel compatibility routes access tables with the service-role key.
-- Flutter authenticates with Supabase and sends its access token to those routes.

create extension if not exists pgcrypto;

-- ------------------------------------------------------------
-- Types
-- ------------------------------------------------------------

do $$ begin
  create type public.user_role as enum ('homebuyer', 'homeowner', 'admin');
exception
  when duplicate_object then null;
end $$;

do $$ begin
  create type public.property_status as enum ('pending', 'approved', 'rejected', 'sold', 'draft');
exception
  when duplicate_object then null;
end $$;

do $$ begin
  create type public.ticket_status as enum ('new', 'open', 'in_progress', 'resolved', 'closed');
exception
  when duplicate_object then null;
end $$;

-- ------------------------------------------------------------
-- Tables
-- ------------------------------------------------------------

create table if not exists public.profiles (
  id bigint generated by default as identity primary key,
  auth_user_id uuid unique references auth.users(id) on delete cascade,
  email text not null unique,
  full_name text,
  phone text,
  role public.user_role not null default 'homebuyer',
  profile_pic text,
  has_password boolean not null default false,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.profiles
  add column if not exists auth_user_id uuid unique references auth.users(id) on delete cascade;
alter table public.profiles add column if not exists phone text;

create table if not exists public.properties (
  id bigint generated by default as identity primary key,
  homeowner_id bigint not null references public.profiles(id) on delete cascade,
  name text not null,
  location text not null,
  price text not null,
  description text not null,
  image_path text not null,
  model_path text not null default '',
  is_featured boolean not null default false,
  status public.property_status not null default 'pending',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.property_images (
  id bigint generated by default as identity primary key,
  property_id bigint not null references public.properties(id) on delete cascade,
  image_url text not null,
  sort_order integer not null default 0,
  created_at timestamptz not null default now()
);

create table if not exists public.favorites (
  id bigint generated by default as identity primary key,
  profile_id bigint not null references public.profiles(id) on delete cascade,
  property_id bigint not null references public.properties(id) on delete cascade,
  created_at timestamptz not null default now(),
  unique (profile_id, property_id)
);

create table if not exists public.inquiries (
  id bigint generated by default as identity primary key,
  homeowner_id bigint not null references public.profiles(id) on delete cascade,
  property_id bigint not null references public.properties(id) on delete cascade,
  property_name text not null,
  property_image text,
  buyer_email text not null,
  message text not null,
  is_read boolean not null default false,
  created_at timestamptz not null default now()
);

create table if not exists public.support_tickets (
  id bigint generated by default as identity primary key,
  profile_id bigint references public.profiles(id) on delete set null,
  user_email text not null,
  subject text not null,
  description text not null,
  attachment_urls jsonb not null default '[]'::jsonb,
  status public.ticket_status not null default 'new',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.notification_preferences (
  profile_id bigint primary key references public.profiles(id) on delete cascade,
  push_notifications boolean not null default false,
  email_notifications boolean not null default false,
  in_app_notifications boolean not null default true,
  updated_at timestamptz not null default now()
);

